package com.whu.dailyexercise.exercisemethod;

public class MethodEntity {
	private int methodid;
	private String methodname;
	private String introduction;
	private String pictureurl;
	private String videourl;
	private float unitheat;
	private int practime;
	private int methodflag;
	public int getMethodid() {
		return methodid;
	}
	public void setMethodid(int methodid) {
		this.methodid = methodid;
	}
	public String getMethodname() {
		return methodname;
	}
	public void setMethodname(String methodname) {
		this.methodname = methodname;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public String getPictureurl() {
		return pictureurl;
	}
	public void setPictureurl(String pictureurl) {
		this.pictureurl = pictureurl;
	}
	public String getVideourl() {
		return videourl;
	}
	public void setVideourl(String videourl) {
		this.videourl = videourl;
	}
	public float getUnitheat() {
		return unitheat;
	}
	public void setUnitheat(float unitheat) {
		this.unitheat = unitheat;
	}
	public int getPractime() {
		return practime;
	}
	public void setPractime(int practime) {
		this.practime = practime;
	}
	public int getMethodflag() {
		return methodflag;
	}
	public void setMethodflag(int methodflag) {
		this.methodflag = methodflag;
	}

}
